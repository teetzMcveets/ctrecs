//ONLY SELECT SUNDAYS ON THE DATE
function validateDate() {
  var selectedDate = new Date(document.getElementById('wk-com-date').value);
  if (selectedDate.getDay() != 0) {
    alert("Please select a Sunday");
    document.getElementById('wk-com-date').value = "";
  }
}

//NEXT AND PREV WEEK
function prevWeek() {
  var selectedDate = new Date(document.getElementById('wk-com-date').value);
  var newDate = new Date(selectedDate.getTime() - 7 * 24 * 60 * 60 * 1000);
  while (newDate.getDay() != 0) {
    newDate = new Date(newDate.getTime() - 24 * 60 * 60 * 1000);
  }
  document.getElementById('wk-com-date').value = newDate.toISOString().substring(0, 10);
}

function nextWeek() {
  var selectedDate = new Date(document.getElementById("wk-com-date").value);
  var newDate = new Date(selectedDate.getTime() + 7 * 24 * 60 * 60 * 1000);
  while (newDate.getDay() != 0) {
    newDate = new Date(newDate.getTime() + 24 * 60 * 60 * 1000);
  }
  document.getElementById("wk-com-date").value = newDate.toISOString().substring(0, 10);
}

